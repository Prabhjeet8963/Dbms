SELECT AVG(SAL) AS Average_Salary
FROM EMP;

SELECT COUNT(*) AS Total_Employees
FROM EMP;

SELECT COUNT(DISTINCT ENAME) AS Distinct_Employees
FROM EMP;

SELECT JOB, SUM(SAL) AS Total_Salary
FROM EMP
GROUP BY JOB;

SELECT *
FROM EMP
WHERE SAL = (SELECT MAX(SAL) FROM EMP);


SELECT *
FROM EMP
WHERE DEPTNO = 10
  AND SAL = (SELECT MAX(SAL) FROM EMP WHERE DEPTNO = 10);

SELECT *
FROM EMP
WHERE SAL = (SELECT (MAX(SAL) + MIN(SAL)) / 2 FROM EMP);


SELECT E1.ENAME, E1.HIREDATE
FROM EMP E1
JOIN EMP E2
  ON E1.HIREDATE = E2.HIREDATE
  AND E1.EMPNO <> E2.EMPNO
ORDER BY E1.HIREDATE;


SELECT ENAME,
       UPPER(ENAME) AS Upper_Case,
       LOWER(ENAME) AS Lower_Case
FROM EMP;

SELECT ENAME, HIREDATE,
       DATE_ADD(HIREDATE, INTERVAL 3 DAY) AS Date_After_3_Days
FROM EMP;